package hwork1;

public class Courses {
	
	int id;
	String courseName;
	String courseDetail;
	String courseInstructor;
	double coursePrice;
	
	public Courses(int id, String courseName , String courseDetail, String courseInstructor, double coursePrice) {
		this.id = id;
		this.courseName = courseName;
		this.courseDetail = courseDetail;
		this.courseInstructor = courseInstructor;
		this.coursePrice = coursePrice;
	}

}
